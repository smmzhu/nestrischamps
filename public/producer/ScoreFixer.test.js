import ScoreFixer from './ScoreFixer.js';

describe('ScoreFixer', () => {
	let fixer;

	beforeEach(() => {
		fixer = new ScoreFixer();
	});

	it('should return null when input is null', () => {
		expect(fixer.fix(null)).toBeNull();
	});

	it('should store initial digits and return them without modification', () => {
		const result = fixer.fix([0x1, 0x2, 0x3, 0x4, 0x5, 0x6]);
		expect(result).toEqual([0x1, 0x2, 0x3, 0x4, 0x5, 0x6]);
		expect(fixer.last_good_digits).toEqual([0x1, 0x2, 0x3, 0x4, 0x5, 0x6]);
	});

	it('should reset last_good_digits when reset() is called', () => {
		fixer.fix([0x0, 0x0, 0x0, 0x0, 0x0, 0x0]);
		fixer.reset();
		expect(fixer.last_good_digits).toBeNull();
	});

	it('should leave score alone if first digit diff is 0 or 1', () => {
		fixer.fix([0x1, 0x2, 0x3, 0x4, 0x5, 0x6]);

		expect(fixer.fix([0x1, 0x5, 0x5, 0x5, 0x5, 0x5])).toEqual([
			0x1, 0x5, 0x5, 0x5, 0x5, 0x5,
		]); // diff 0
		expect(fixer.fix([0x2, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0x2, 0x0, 0x0, 0x0, 0x0, 0x0,
		]); // diff 1
	});

	it('should fix A to 4 if last was 3 or 4', () => {
		fixer.fix([0x3, 0x9, 0x9, 0x9, 0x9, 0x9]);
		expect(fixer.fix([0xa, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0x4, 0x0, 0x0, 0x0, 0x0, 0x0,
		]);

		fixer.reset();
		fixer.fix([0x4, 0x5, 0x0, 0x0, 0x0, 0x0]);
		expect(fixer.fix([0xa, 0x6, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0x4, 0x6, 0x0, 0x0, 0x0, 0x0,
		]);
	});

	it('should fix 4 to A if last was 9 or A', () => {
		fixer.fix([0x9, 0x9, 0x9, 0x9, 0x9, 0x9]);
		expect(fixer.fix([0x4, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0xa, 0x0, 0x0, 0x0, 0x0, 0x0,
		]);

		fixer.reset();
		fixer.fix([0xa, 0x0, 0x0, 0x0, 0x0, 0x0]);
		expect(fixer.fix([0x4, 0x1, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0xa, 0x1, 0x0, 0x0, 0x0, 0x0,
		]);
	});

	it('should fix 8 to B if last was A or B', () => {
		fixer.fix([0xa, 0x9, 0x9, 0x9, 0x9, 0x9]);
		expect(fixer.fix([0x8, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0xb, 0x0, 0x0, 0x0, 0x0, 0x0,
		]);
	});

	it('should fix 8 to D if last was C or D', () => {
		fixer.fix([0xc, 0x9, 0x9, 0x9, 0x9, 0x9]);
		expect(fixer.fix([0x8, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0xd, 0x0, 0x0, 0x0, 0x0, 0x0,
		]);
	});

	it('should fix B to 8 if last was 7 or 8', () => {
		fixer.fix([0x7, 0x9, 0x9, 0x9, 0x9, 0x9]);
		expect(fixer.fix([0xb, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0x8, 0x0, 0x0, 0x0, 0x0, 0x0,
		]);
	});

	it('should fix B to D if last was C or D', () => {
		fixer.fix([0xc, 0x9, 0x9, 0x9, 0x9, 0x9]);
		expect(fixer.fix([0xb, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0xd, 0x0, 0x0, 0x0, 0x0, 0x0,
		]);
	});

	it('should fix 0 to D if last was C or D', () => {
		fixer.fix([0xc, 0x9, 0x9, 0x9, 0x9, 0x9]);
		expect(fixer.fix([0x0, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0xd, 0x0, 0x0, 0x0, 0x0, 0x0,
		]);
	});

	it('should fix D to 0 if last was 0 or F', () => {
		// e.g. score wrapped 1.6M -> 0, OCR misreads 0 as D
		// wait, F -> 0
		fixer.fix([0xf, 0x9, 0x9, 0x9, 0x9, 0x9]);
		expect(fixer.fix([0xd, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
		]);
	});

	it('should fix E to 6 if last was 5 or 6', () => {
		fixer.fix([0x5, 0x9, 0x9, 0x9, 0x9, 0x9]);
		expect(fixer.fix([0xe, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0x6, 0x0, 0x0, 0x0, 0x0, 0x0,
		]);
	});

	it('should fix 6 to E if last was D or E', () => {
		// 6 to E
		fixer.fix([0xd, 0x9, 0x9, 0x9, 0x9, 0x9]);
		expect(fixer.fix([0x6, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0xe, 0x0, 0x0, 0x0, 0x0, 0x0,
		]);
	});

	it('should return unhandled misreads unmodified', () => {
		// e.g. huge jump, not in conditions
		fixer.fix([0x1, 0x0, 0x0, 0x0, 0x0, 0x0]);
		expect(fixer.fix([0x8, 0x0, 0x0, 0x0, 0x0, 0x0])).toEqual([
			0x8, 0x0, 0x0, 0x0, 0x0, 0x0,
		]);
	});

	it('should handle charToDigit and digitToChar methods', () => {
		expect(fixer.charToDigit('a')).toBe(10);
		expect(fixer.charToDigit('A')).toBe(10);
		expect(fixer.charToDigit('5')).toBe(5);

		expect(fixer.digitToChar(10)).toBe('A');
		expect(fixer.digitToChar(15)).toBe('F');
		expect(fixer.digitToChar(5)).toBe('5');
	});
});
